export function recalculateMockSummary() {
  const rooms = mockGmStateResponse.rooms;
  mockGmStateResponse = {
    ...mockGmStateResponse,
    summary: {
      ...mockGmStateResponse.summary,
      rooms_total: rooms.length,
      active_sessions: rooms.filter(
        (room) => room.session_state === "running" || room.session_state === "paused",
      ).length,
      active_hints: rooms.filter((room) => room.hint_active).length,
    },
  };
}

